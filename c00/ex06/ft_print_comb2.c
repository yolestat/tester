#include <unistd.h>

char	ft_print(char c0, char c1, char c2, char c3)
{
	write(1, &c0, 1);
	write(1, &c1, 1);
	write(1, &" ", 1);
	write(1, &c2, 1);
	write(1, &c3, 1);
	write(1, &", ", 2);
	return 0;
}

void	ft_print_comb2(void)
{
	char	n[4];

	n[0] = '0';
	n[1] = '0';
	n[2] = '0';
	n[3] = '/';
	while (n[3]++ < '9')
	{
		ft_print((n[0]), (n[1]), (n[2]), (n[3]));
		if ((n[1] == '9') && (n[0] != 0) && (n[0] < '9'))
		{
			n[0]++;
			n[1] = 48;
		}
		if ((n[2] == '9') && (n[3] == '9') && (n[1] < '9'))
		{
			n[1]++;
			n[2] = 48;
		}
		if ((n[3] == '9') && (n[2] != 0) && (n[2] < '9'))
		{
			n[2]++;
			n[3] = 47;
		}
	}
	
}

int	main(void)
{
	ft_print_comb2();
	return 0;
}
